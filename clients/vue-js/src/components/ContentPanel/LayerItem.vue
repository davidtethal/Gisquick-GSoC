<template>
    <!-- Group Item -->
    <div
      v-if="layer.layers"
      class="group-container">
      <div
        class="item group"
        :depth="depth">
        <v-btn
          icon
          @click.stop="toggleCollapsed">
          <v-icon>{{ this.collapsed ? 'add' : 'remove' }}</v-icon>
        </v-btn>
        <label>{{ layer.title }}</label>
        <v-switch
          v-model="layer.visible"
          color="primary"
          @change="toggleGroupVisibility"
          hide-details />
      </div>
      <v-collapsible v-show="!collapsed" :class="{disabled: !layer.visible}">
        <layer-item
          v-for="l in layer.layers"
          :key="l.title"
          :layer="l"
          :expanded="expanded"
          :depth="depth + 1"
          @changed:visibility="$emit('changed:visibility')"
          @expanded="(id) => $emit('expanded', id)">
          <layer-slot slot="custom" slot-scope="props" :node="$scopedSlots.custom(props)"/>
        </layer-item>
      </v-collapsible>
    </div>

    <!-- Layer Item -->
    <div
      v-else-if="!layer.hidden"
      class="item-container"
      :class="{expanded: isExpanded}">
      <div class="item">
        <v-checkbox
          color="primary"
          v-model="layer._visible"
          :label="layer.title || layer.name"
          @change="$emit('changed:visibility')"
          hide-details>
        </v-checkbox>
        <v-btn
          icon
          class="expand"
          @click.stop="$emit('expanded', layer.name)">
            <v-icon>keyboard_arrow_down</v-icon>
        </v-btn>
      </div>
      <collapse-transition>
        <div v-if="isExpanded" class="metadata">
          <div class="pb-1" />
          <label>Geometry:</label>
          <icon :name="layer.geom_type? layer.geom_type.toLowerCase() : 'raster'" /><br />

          <label>Identification:</label>
          <icon :name="layer.queryable? 'check' : 'dash'" /><br />

          <label>Abstract:</label>
          <span>{{ layer.metadata.abstract }}</span><br />

          <label>Keywords list:</label>
          <span>{{ layer.metadata.keyword_list }}</span><br />

  <!--           <label>Maximal scale:</label>
          <span> 1: {{ layer.visibility_scale_min }}</span><br /> -->
          <div class="pb-1" />
        </div>
      </collapse-transition>
      <slot name="custom" :layer="layer"/>
    </div>

</template>

<script>
import Vue from 'vue'

const LayerSlot = {
  functional: true,
  props: ['node'],
  render (h, context) {
    return context.props.node
  }
}

export default Vue.component('layer-item', {
  components: { LayerSlot },
  props: ['layer', 'expanded', 'depth'],
  data: () => ({
    collapsed: false
  }),
  computed: {
    isExpanded () {
      return this.expanded === this.layer.name
    }
  },
  methods: {
    toggleGroupVisibility () {
      this.$emit('changed:visibility')
    },
    toggleCollapsed () {
      this.collapsed = !this.collapsed
    },
    slotNode (layer) {
      return this.$scopedSlots.custom({layer})
    }
  }
})

</script>

